package aula11;

public class VariaveisPontoFlutuante {
    public static void main(String[] args) {

        /* Ao utilizar float, sempre colocar o f no final, porém essa variável é pouco utilizada, preferencialmente se usa o double*/
        float valorPassagem = 4.40f;

        double valorLaranja = 9.3;

        System.out.println("Valor da passagem = " + valorPassagem);
        System.out.println("Valor da Laranja = " + valorLaranja);
    }
}
